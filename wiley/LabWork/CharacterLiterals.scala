package com.wiley.LabWork

object CharacterLiterals extends App{
  //Character Literals are extremely useful for formatting certain
  //prints statements as required
  //backspace
  println("Hello \b World")
  //horizontal tab
  println("Hello \t World")
  //line feed
  println("Hello \nWorld")
  //form feed
  println("Hello \f World")
  //carriage return
  println("Hello \r World")
  //double quote
  println("Hello \"World\"")
  //single quote
  println("Hello \'World\'")
  //backslash
  println("Hello \\ World")

  // Power point activity
  println("This is a \n test\" \n     \"He said, \\\"SQL is for database!\\\"\" \n     \"First \t Second")

  // Printing an error
  // Note that this does not stop the flow of execution 
  System.err.println("yikes, an error happened")


}
